package uuu.etgt.test;
import java.util.HashMap;

public class tryHashMap {

	public static void main(String[] args) {
		
		System.out.printf("右邊直角三角形\n");
	
		for(int i=1;i<=5;i++){
		    for(int j=5;j>0;j--){
		        if(j<=i){
		             System.out.print("*");
		        }
		        else{
		             System.out.print(" ");
		        }
		  }
		  System.out.print("\n");
		}
		
	}

}
